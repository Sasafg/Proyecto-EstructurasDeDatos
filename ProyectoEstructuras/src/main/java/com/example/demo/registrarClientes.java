    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String fechaNaci = txtFechaNaci.getText();
        String correo = txtCorreo.getText();
        String categoria = (String) jComboCategoria.getSelectedItem();

        if (this.txtCedula.getText().equals("") && this.txtNombre.getText().equals("")
                && this.txtFechaNaci.getText().equals("") && this.jComboCategoria.getSelectedItem().equals("SELECCIONE UNA OPCIÓN")) {
            JOptionPane.showMessageDialog(null, "Informacion Incompleta");

        } else if (this.txtNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Informacion Incompleta. Por favor ingrese el nombre del cliente.");

        } else if (this.txtCedula.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Informacion Incompleta. Por favor ingrese el número de cédula.");

        } else if (this.txtFechaNaci.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Informacion Incompleta. Por favor ingrese la fecha de nacimiento.");

        } else if (this.jComboCategoria.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(null, "Informacion Incompleta. Por favor elija una categoría.");

        } else if (this.txtNombre.getText().matches(".*[0-9].*")) {
            JOptionPane.showMessageDialog(null, "Por favor digite un nombre válido.");
            //this.txtNombre.setText("");

        } else if (!this.txtCedula.getText().matches("[0-9]+") || this.txtCedula.getText().length() < 9 || this.txtCedula.getText().length() > 9) {
            JOptionPane.showMessageDialog(null, "Por favor digite un número de cédula válido. "
                    + "\nNo use ningún tipo de signo para separar los números y recuerde ingresar 9 números.");
            //this.txtCedula.setText("");

        } else if (!this.txtFechaNaci.getText().matches("^([0-2][0-9]|3[0-1])(\\/)(0[1-9]|1[0-2])\\2(\\d{4})$")) {
            JOptionPane.showMessageDialog(null, "Por favor digite una fecha válida. \nUtilice solamente \"/\" para separación y siga el formato dd/mm/yyyy. ");
            //this.txtFechaNaci.setText("");

        } else if (this.txtCorreo.getText().matches("^[\\w-+]+(\\.[\\w-]{1,62}){0,126}@[\\w-]{1,63}(\\.[\\w-]{1,62})+/[\\w-]+$")) {
            JOptionPane.showMessageDialog(null, "Por favor digite una dirección de correo electrónico válido.");
            //this.txtCorreo.setText("");

        } else {
            Cliente info = new Cliente(Integer.parseInt(cedula), nombre, fechaNaci, correo, categoria);
            lista.registrar(info);

            Object datos[] = {cedula, nombre, fechaNaci, correo, categoria};
            md.addRow(datos);

            //lista.mostrarRegistros();
            JOptionPane.showMessageDialog(null, "Informacion guardada satisfactoriamente");
            limpiarCajas();

        }
    }

    private void limpiarCajas() {
        this.txtCedula.setText("");
        this.txtNombre.setText("");
        this.txtFechaNaci.setText("");
        this.txtCorreo.setText("");
        this.jComboCategoria.setSelectedIndex(0);
    }

