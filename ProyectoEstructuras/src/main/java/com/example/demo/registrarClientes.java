    private void btnEditarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarRegistroActionPerformed
        String cedulaBuscada = JOptionPane.showInputDialog("Ingrese el número de cédula del cliente a eliminar.");

        if (cedulaBuscada.equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el número de cédula.");

        } else if (!cedulaBuscada.matches("[0-9]+") || cedulaBuscada.length() < 9 || cedulaBuscada.length() > 9) {
            JOptionPane.showMessageDialog(null, "Por favor digite un número de cédula válido. "
                    + "\nNo use ningún tipo de signo para separar los números y recuerde ingresar 9 números.");
        } else {
            modificacionCliente ventanaMod = new modificacionCliente(Integer.parseInt(cedulaBuscada), this.lista);
            ventanaMod.setTitle("Modificación de cliente");
            ventanaMod.setLocationRelativeTo(null);
            ventanaMod.setVisible(true);
            this.dispose();
        }

    }//GEN-LAST:event_btnEditarRegistroActionPerformed

